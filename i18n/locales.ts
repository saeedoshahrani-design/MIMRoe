// FIX: Add a more specific type to help TypeScript understand the shape of the exported object.
type ExtendedLocale = Locale & {
    formatDate: (dateString?: string | null) => string;
    formatNumber: (num: number) => string;
};

const formatDateDDMMYYYY = (dateString?: string | null): string => {
    if (!dateString) return '-';
    try {
        const date = new Date(dateString);
        if (isNaN(date.getTime())) return '-';
        // Using en-GB locale provides the DD/MM/YYYY format consistently.
        return new Intl.DateTimeFormat('en-GB', {
            day: '2-digit',
            month: '2-digit',
            year: 'numeric'
        }).format(date);
    } catch (e) {
        console.error("Error formatting date:", e);
        return '-';
    }
};

export const locales: { en: ExtendedLocale; ar: ExtendedLocale } = {
    en: { ...en, formatDate: formatDateDDMMYYYY, formatNumber: (num: number) => new Intl.NumberFormat('en-US').format(num) },
    ar: { ...ar, formatDate: formatDateDDMMYYYY, formatNumber: (num: number) => new Intl.NumberFormat('ar-SA').format(num) },
};
